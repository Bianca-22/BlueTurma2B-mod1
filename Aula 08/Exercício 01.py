aLista = [5, 7, 2, 9, 4, 1, 3]
print(len(aLista)) #Tamanho da lista
print(max(aLista)) #Maior valor da lista
print(min(aLista)) #Menor valor da lista 
print(sum(aLista)) #Soma de todos os valores da lista
aLista.sort() #Ordena em ordem crescente
print(aLista)
aLista.sort(reverse=True) #Ordena em ordem decrescente
print(aLista)